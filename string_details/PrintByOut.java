package string_details;

/**
 * PrintByOut
 */
public class PrintByOut {

  public static void main(String[] args) {
    int x = 10;
    int y = 20;
    System.out.println("Sum is " + x + y); // Sum is 1020
    System.out.println("Sum is " + (x + y)); // Sum is 30
    System.out.println("Sum of " + x + " and " + y + " is " + x + y); // Sum of 10 and 20 is 1020
  }
}